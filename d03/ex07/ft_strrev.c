char	*ft_strrev(char *str)
{
	int l;
	int i;
	int tmp;

	l = 0;
	i = 0;
	while (str[l] != '\0')
	{
		l++;
	}
	l--;
	while (i < l)
	{
		tmp = str[l];
		str[l] = str[i];
		str[i] = tmp;
		i++;
		l--;
	}
	return (str);
}
