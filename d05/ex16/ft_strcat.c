char	*ft_strcat(char *dest, char *src)
{
	int i;
	int l;

	i = 0;
	l = 0;
	while (dest[l])
	{
		l++;
	}
	while (src[i])
	{
		dest[l] = src[i];
		l++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
